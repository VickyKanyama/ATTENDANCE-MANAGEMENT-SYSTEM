public class Attendance {
    private int adminNumber;
    private String Sname;

    public Attendance(int adminNumber, String Sname) {
        this.adminNumber = adminNumber;
        this.Sname = Sname;
    }

    public int getAdminnumber() {
        return adminNumber;
    }

    public String getSname() {
        return Sname;
    }
}















